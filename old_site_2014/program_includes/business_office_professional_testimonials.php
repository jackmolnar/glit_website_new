<p><img height="147" alt="Business Office Professional" width="120" align="left" src="http://www.erieit.edu/_files/images/file_31.jpg" />for applicants with extensive knowledge of software applications and for experienced secretaries and administrative assistants. Opportunities also should be very good for those with advanced communication and computer skills. <br />

<br />

Developments in office technology are certain to continue. However, many secretarial and administrative duties are of a personal, interactive nature and, therefore, are not easily automated. Responsibilities such as planning conferences, working with clients, and instructing staff require tact and communication skills. Because technology cannot substitute for these personal skills, secretaries and administrative assistants will continue to play a key role in most organizations.<br />

<br />

&ldquo;Administrative professionals are no longer simply answering phones and handling correspondence,&rdquo; said IAAP President Barb Horton CAP. &ldquo;Admins of today are buying software, learning how to use it and training the management. In addition, our latest survey shows that admins are branching out into non-traditional roles including budget analysis, research, project management and internal communications.&rdquo; <br />

